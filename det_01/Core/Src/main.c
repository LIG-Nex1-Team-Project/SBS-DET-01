uint16_t SG90_CCRToANGLE(int CCR)
{
	double ccr_diff = CCR - SERVO_MIN;
	double ccr_diff2 = SERVO_MAX - SERVO_MIN;
	double angle = ccr_diff / ccr_diff2 * 180;
	return angle;
}

